#!/usr/bin/env bun
/**
 * Generates public-assets.ts — a module that embeds all files from public/
 * as a Map<string, { content: string | Uint8Array; mime: string }>.
 *
 * Run: bun run scripts/gen-assets.ts
 * Output: public-assets.ts (project root)
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, extname } from "node:path";

const PUBLIC_DIR = join(import.meta.dirname, "..", "public");
const OUT_FILE = join(import.meta.dirname, "..", "src", "public-assets.ts");

const MIME_MAP: Record<string, string> = {
  ".html": "text/html; charset=utf-8",
  ".json": "application/json",
  ".js": "application/javascript",
  ".css": "text/css",
  ".png": "image/png",
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".svg": "image/svg+xml",
  ".ico": "image/x-icon",
  ".webp": "image/webp",
  ".woff2": "font/woff2",
  ".woff": "font/woff",
  ".ttf": "font/ttf",
};

const TEXT_EXTS = new Set([".html", ".json", ".js", ".css", ".svg", ".xml"]);

function isText(ext: string): boolean {
  return TEXT_EXTS.has(ext);
}

const files = readdirSync(PUBLIC_DIR).sort();
const entries: string[] = [];

for (const file of files) {
  const ext = extname(file).toLowerCase();
  const mime = MIME_MAP[ext] ?? "application/octet-stream";
  const filePath = join(PUBLIC_DIR, file);

  if (isText(ext)) {
    const content = readFileSync(filePath, "utf-8");
    // Use JSON.stringify to safely embed the string
    entries.push(
      `  [${JSON.stringify(file)}, { content: ${JSON.stringify(content)}, mime: ${JSON.stringify(mime)} }]`,
    );
  } else {
    // Binary: embed as base64, decode at import time
    const buf = readFileSync(filePath);
    const b64 = buf.toString("base64");
    entries.push(
      `  [${JSON.stringify(file)}, { content: _b64(${JSON.stringify(b64)}), mime: ${JSON.stringify(mime)} }]`,
    );
  }
}

const output = `// AUTO-GENERATED by scripts/gen-assets.ts — do not edit
// Embeds all files from public/ for bun compile single-binary distribution.

function _b64(s: string): Uint8Array {
  return Buffer.from(s, "base64");
}

export const assets = new Map<string, { content: string | Uint8Array; mime: string }>([
${entries.join(",\n")},
]);
`;

writeFileSync(OUT_FILE, output);
console.log(`generated ${OUT_FILE} (${files.length} files embedded)`);
